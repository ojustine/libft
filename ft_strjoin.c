#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char *new;
	char *ret;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	new = ft_strnew(ft_strlen(s1) + ft_strlen(s2));
	if (!new)
		return (NULL);
	ret = new;
	while (*s1)
		*new++ = *s1++;
	while (*s2)
		*new++ = *s2++;
	return (ret);
}
